import React, {useState, useRef} from 'react';
import Icon from 'react-native-vector-icons/Ionicons';
import {Form} from '@unform/mobile';
import Input from '../../unform/input';
import {createUser} from '../../../backend/firebase/registerFB';
import ImagePicker from 'react-native-image-picker';
import {validateRegister} from '../../../backend/validations';
import {ScrollView} from 'react-native';
import ToastDefault from '../../toasts';
import ProgressLottie from '../../../assets/progressLottie.json';
import {LottieLoading} from '../../lottieLoading';
import {
  Container,
  ButtonIcon,
  TitleHeaderLight,
  TextButtonLight,
  ViewForm,
  HeaderGradient,
  HeaderProfile,
  ButtonCamera,
  wh,
} from '../../stylesShared';
import {PhotoHired, MarginPhoto, ButtonRegister} from './styles';

export default function Register({route, navigation}) {
  const [avatar, setAvatar] = useState();
  const [modalLoading, setModalLoading] = useState(false);
  const formRef = useRef(null);
  const {isClient} = route.params;

  const optionsImagePicker = {
    title: 'Selecione uma foto de perfil:',
    chooseFromLibraryButtonTitle: 'Galeria',
    takePhotoButtonTitle: 'Tirar uma foto',
    cancelButtonTitle: 'Cancelar',
  };

  async function handleRegister(data) {
    data.avatar = avatar;
    //if (validateRegister(data) === true) {
    setModalLoading(true);
    data.isClient = isClient;
    //data.avatar = avatar.path;
    const response = await createUser(data);
    setModalLoading(false);
    if (response === false) {
      ToastDefault('Cadastro realizado!');
      navigation.navigate('Login');
    } else {
      ToastDefault(response);
    }
    // } else {
    ToastDefault(validateRegister(data));
    // }
  }

  function imagePickerCallback(data) {
    if (data.didCancel) {
      return;
    }
    if (data.error) {
      return;
    }
    if (!data.uri) {
      return;
    }
    setAvatar(data);
  }

  return (
    <Container>
      <ScrollView>
        <ViewForm>
          <Form ref={formRef} onSubmit={handleRegister}>
            <Input
              icon="ios-contact"
              iconFamily="Ionicons"
              iconSize={28}
              label="Nome:"
              name="name"
              textContentType="name"
              autoCompleteType="name"
              backgroundColor="rgba(0, 0, 131, 0.5)"
              autoCorrect={false}
              placeholder="Seu nome"
              placeholderTextColor="rgba(255, 255, 255, 0.5)"
            />
            <Input
              icon="ios-mail"
              iconFamily="Ionicons"
              iconSize={28}
              label="E-mail:"
              name="email"
              backgroundColor="rgba(0, 0, 131, 0.5)"
              autoCorrect={false}
              autoCompleteType="email"
              textContentType="emailAddress"
              keyboardType="email-address"
              placeholder="Seu email"
              placeholderTextColor="rgba(255, 255, 255, 0.5)"
            />
            <Input
              icon="ios-call"
              iconFamily="Ionicons"
              iconSize={28}
              label="Telefone:"
              name="phone"
              backgroundColor="rgba(0, 0, 131, 0.5)"
              autoCorrect={false}
              autoCompleteType="tel"
              textContentType="emailAddress"
              keyboardType="phone-pad"
              placeholder="Seu telefone com DDD"
              placeholderTextColor="rgba(255, 255, 255, 0.5)"
            />
            <Input
              icon="ios-lock"
              iconFamily="Ionicons"
              iconSize={28}
              label="Senha:"
              name="password"
              backgroundColor="rgba(0, 0, 131, 0.5)"
              placeholderTextWeigth="bold"
              autoCorrect={false}
              secureTextEntry={true}
              placeholder="Sua senha"
              placeholderTextColor="rgba(255, 255, 255, 0.5)"
            />

            <ButtonRegister onPress={() => formRef.current.submitForm()}>
              <TextButtonLight>Cadastrar-se</TextButtonLight>
            </ButtonRegister>
          </Form>
        </ViewForm>
        <HeaderGradient colors={['#000054', '#000074', '#000094']}>
          <HeaderProfile>
            <ButtonIcon onPress={() => navigation.goBack()}>
              <Icon name="ios-arrow-back" size={27} color="#fff" />
            </ButtonIcon>
            <TitleHeaderLight>Cadastro</TitleHeaderLight>
            <ButtonIcon />
          </HeaderProfile>
          <MarginPhoto>
            <PhotoHired
              source={{
                uri: avatar
                  ? avatar.uri
                  : 'https://upload.wikimedia.org/wikipedia/commons/thumb/2/24/Missing_avatar.svg/1024px-Missing_avatar.svg.png',
              }}
            />
            <ButtonCamera
              onPress={() =>
                ImagePicker.showImagePicker(
                  optionsImagePicker,
                  imagePickerCallback,
                )
              }>
              <Icon name="ios-camera" size={32} color="#000084" />
            </ButtonCamera>
          </MarginPhoto>
        </HeaderGradient>
        <LottieLoading
          visible={modalLoading}
          source={ProgressLottie}
          size={8}
        />
      </ScrollView>
    </Container>
  );
}
